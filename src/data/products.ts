export function getProductsByTag(tag: string): Product[] {
  return products.filter((p) => p.tags.includes(tag));
}

export function getProductsByCategory(category: Product['category']): Product[] {
  return products.filter((p) => p.category === category);
}

/** Альтернативы в той же категории приёма пищи */
export function getAlternatives(product: Product, mealType: string): Product[] {
  const mealTag = mealType === 'breakfast' ? 'breakfast'
    : mealType === 'snack' ? 'snack'
    : ['lunch', 'dinner'].includes(mealType) ? mealType
    : null;

  return products.filter((p) => {
    if (p.id === product.id) return false;
    if (p.category !== product.category) return false;
    if (mealTag && !p.tags.includes(mealTag) && !p.tags.includes('cooking')) return false;
    return true;
  });
}
